<?php
class SimpleCalculator
{
    var $num1;
    var $num2;
    function add($num1, $num2){
        return $num1 + $num2;
    }
    function sub($num1, $num2){
        return $num1 - $num2;
    }
    function mul($num1, $num2){
        return $num1 * $num2;
    }
    function div($num1, $num2){
        return $num1 / $num2;
    }


}
?>